package c1_KNN;

public class DataObject {
    int x;
    int y;
    int klasse;
    public DataObject (int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    
    public DataObject (int x, int y, int klasse)
    {
        this.x = x;
        this.y = y;
        this.klasse = klasse;
    }
}
